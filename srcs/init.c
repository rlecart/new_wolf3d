void	init_values(t_env *e)
{
	e->pos.x = 1.5;
	e->pos.y = 1.5;
	e->dir.x = -1;
	e->dir.y = 0;
	e->plane.x = 0;
	e->plane.y = 0.66;
}

void	init(t_env *e)
{
	MLX = mlx_init();
	WIN = mlx_new_window(MLX, WIN_W, WIN_H, "Wolf3D");
	IMG.img = mlx_new_image(MLX, WIN_W, WIN_H);
	IMG.data = mlx_get_data_addr(IMG.img, &IMG.bpp, &IMG.size_l, &IMG.endian);
	mlx_key_hook(WIN, key_hook, e);
	mlx_hook(WIN, 17, 0L, exit_hook, e);
	init_matrix(e);
	init_values(e);
}
